def sumar(a, b):
    return a + b

def restar(a, b):
    return a - b

def multiplicar(a, b):
    return a * b

def dividir(a, b):
    return a / b

x = float(input("Primer número: "))
y = float(input("Segundo número: "))

print(f"La suma es {sumar(x,y)}")

print(f"La resta es {restar(x,y)}")

print(f"La multiplicación es {multiplicar(x,y)}")

print(f"La dividir es {dividir(x,y)}")



